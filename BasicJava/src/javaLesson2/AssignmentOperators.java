package javaLesson2;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		//assignment operators =, +=, -=, *=, /=, %=
		
		int x=5, y=3;
		
		System.out.println(x);
	
    	x+=y;
		System.out.println(x);
		
		x-=y;
		System.out.println(x);
		
		x*=y;
		System.out.println(x);
		
		x/=y;
		System.out.println(x);
		
		x%=y;
		System.out.println(x); 

	}

}
